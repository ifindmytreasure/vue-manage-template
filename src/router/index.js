/**
 * hidden: true                 如果设置为true，项目将不会显示在侧栏(默认为false)
 * alwaysShow: true             如果设置为true，将始终显示根菜单
 *                              如果不设置alwaysShow，当项目有多个子路由时，
 *                              它将成为嵌套模式，否则不显示根菜单
 * redirect: noRedirect         禁止重定向
 * name:'router-name'            路由名称用于keep-alive 必须设置
 * meta : {
    roles: ['admin']            控制页面角色（可多个）
    title: 'title'              名称显示在侧栏和面包屑
    icon: 'svg-name'            图标显示在侧栏中
    noCache: true               如果设置true，不进行缓存
    affix: true                 如果设置为真，则标记将附加在标记视图中
    breadcrumb: false           如果设置为false，则该项将隐藏在breadcrumb中(默认为true)
    activeMenu: '/example/list'  如果设置路径，侧栏将突出显示您设置的路径
  }
 */
